def clear_resources():
    session = get_session()
    print("正在删除旧的学习资源...")
    session.query(LearningResource).delete()
    session.commit()
    session.close()
    print("旧数据删除完成！")

def generate_resources():
    session = get_session()
    
    resources = []
    resource_id = 1
    
    for grade, grade_info in grade_data.items():
        subjects = grade_info["subjects"]
        titles = grade_info["titles"]
        knowledge_points = grade_info["knowledge_points"]
        
        for subject in subjects:
            subject_titles = titles.get(subject, [])
            subject_kps = knowledge_points.get(subject, [])
            
            for title_idx, title in enumerate(subject_titles):
                resource_type = random.choice(resource_types)
                difficulty = random.choice(difficulties)
                
                num_kps = random.randint(2, min(4, len(subject_kps)))
                selected_kps = random.sample(subject_kps, num_kps) if subject_kps else []
                
                num_tags = random.randint(1, 3)
                selected_tags = random.choice(tags_list)[:num_tags]
                
                description = random.choice(descriptions)
                
                created_at = datetime.utcnow() - timedelta(days=random.randint(0, 90))
                
                resource = LearningResource(
                    title=f"{title}",
                    description=description,
                    resource_type=resource_type,
                    content_url=f"https://example.com/resources/{resource_id}",
                    content=f"这是{title}的详细内容，包含了丰富的知识点和示例。适用于{grade}的{subject}学习。",
                    knowledge_points=selected_kps,
                    subject=subject,
                    difficulty=difficulty,
                    tags=[grade] + selected_tags,
                    view_count=random.randint(0, 5000),
                    created_at=created_at
                )
                
                resources.append(resource)
                session.add(resource)
                resource_id += 1
    
    session.commit()
    print(f"成功生成 {len(resources)} 个学习资源！")
    
    print("\n各年级资源数量统计：")
    grade_counts = {}
    for grade in grade_data.keys():
        count = sum(1 for r in resources if grade in r.tags)
        grade_counts[grade] = count
        print(f"  {grade}: {count} 个")
    
    session.close()
    return resources

